const mongoose = require("mongoose");

const SlotSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      max: 500,
    },
    img: {
      type: String,
    },
    likes: {
      type: Array,
      default: [],
    },
  },
  { timestamps: true }
);

Slot = mongoose.model("Slot", SlotSchema);
 
module.exports = Slot;